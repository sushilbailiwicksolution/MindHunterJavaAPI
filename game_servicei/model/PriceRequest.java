package com.bailiwick.game_servicei.model;

public class PriceRequest {
	private int entryFees;
	private int instanceId;
	private int marginePrice;
	public int getEntryFees() {
		return entryFees;
	}
	public void setEntryFees(int entryFees) {
		this.entryFees = entryFees;
	}
	public int getInstanceId() {
		return instanceId;
	}
	public void setInstanceId(int instanceId) {
		this.instanceId = instanceId;
	}
	public int getMarginePrice() {
		return marginePrice;
	}
	public void setMarginePrice(int marginePrice) {
		this.marginePrice = marginePrice;
	}
	

}
